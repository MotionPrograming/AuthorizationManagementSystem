CREATE TABLE APPROVAL (

    APPROVAL_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,


    REQUEST_ID NUMBER NOT NULL,


    APPROVER_ID NUMBER NOT NULL,


    DECISION VARCHAR2(20)
    CHECK(
        DECISION IN ('APPROVED','REJECTED')
    ),


    COMMENTS VARCHAR2(500),


    APPROVED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,


    CONSTRAINT FK_APPROVAL_REQUEST
    FOREIGN KEY(REQUEST_ID)
    REFERENCES ACCESS_REQUEST(REQUEST_ID),


    CONSTRAINT FK_APPROVAL_USER
    FOREIGN KEY(APPROVER_ID)
    REFERENCES USERS(USER_ID)

);